$("#class-name").click(function() {
    var year = $("#year").val();
    if(year === "2019") {
        alert("Senior");
    }    
    else if (year === "2020") {
        alert("Junior");
}
 else if (year === "2021") {
        alert("Sophomore");
}
 else if (year === "2022") {
        alert("Freshman");
}
});